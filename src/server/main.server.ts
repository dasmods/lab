import { ReplicatedStorage } from "@rbxts/services";
import { t } from "@rbxts/t";
import TestEZ from "@rbxts/testez";

const TESTS = ReplicatedStorage.FindFirstChild("TS")?.FindFirstChild("tests");
assert(t.instanceIsA("Folder")(TESTS));

const results = TestEZ.TestBootstrap.run([TESTS]);
if (results.errors.size() > 0 || results.failureCount > 0) {
	error("Tests failed!");
}
